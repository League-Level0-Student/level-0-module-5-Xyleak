package nested_loops;

public class For_Loop_Gauntlet {
	
public static void main(String[] args) {
	
	for (int i = 0; i < 101; i++) {
		System.out.println(i);
	
		
}
	
	for (int e = 100; e >= 0; e--) {
		System.out.println(e);
		
}
	
	for (int o = 2; o < 101; o++) { 
	System.out.println(o);
	}
	
}	
}